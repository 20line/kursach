from logging import exception  # импорт не используется в коде, возможно, забыт
from pymysql import connect
from pymysql.err import OperationalError


class DBContextManager:
    """
    Контекстный менеджер для работы с базой данных MySQL через pymysql.
    Автоматически управляет подключением, транзакциями и освобождением ресурсов.
    Используется с оператором with.
    """

    def __init__(self, db_connect: dict):
        """
        Инициализатор экземпляра контекстного менеджера.
        :param db_connect: словарь с параметрами подключения к БД
        (host, user, password, database и т.д.)
        """
        self.conn = None          # соединение с БД, изначально отсутствует
        self.cursor = None        # курсор для выполнения запросов, изначально отсутствует
        self.db_connect = db_connect  # сохраняем параметры подключения для дальнейшего использования

    def __enter__(self):
        """
        Метод, вызываемый при входе в блок with.
        Устанавливает соединение с БД, создаёт курсор и начинает транзакцию.
        Возвращает курсор, который будет доступен в блоке as cursor.
        В случае ошибки подключения возвращает None, чтобы вызывающий код мог обработать ситуацию.
        """
        try:
            # Устанавливаем соединение, распаковывая словарь с параметрами
            self.conn = connect(**self.db_connect)
            # Создаём курсор — объект для выполнения SQL-запросов и получения результатов
            self.cursor = self.conn.cursor()
            # Явно начинаем транзакцию. Все последующие запросы будут частью одной транзакции
            self.conn.begin()
            # Возвращаем курсор для использования внутри блока with
            return self.cursor
        except OperationalError as err:
            # Ошибка подключения (например, неверные параметры, БД не запущена)
            # Выводим аргументы ошибки в консоль (для отладки)
            print(err.args())
            # Возвращаем None, сигнализируя, что курсор не создан
            return None

    def __exit__(self, exc_type, exc_val, exc_tb):
        """
        Метод, вызываемый при выходе из блока with (как при нормальном завершении, так и при исключении).
        :param exc_type: класс исключения, если произошла ошибка; иначе None
        :param exc_val: экземпляр исключения
        :param exc_tb: объект traceback (информация о стеке вызовов)
        :return: True — подавляет исключение (оно не выбрасывается дальше),
                 False или None — исключение пробрасывается.
        Здесь возвращается True всегда, чтобы после блока with выполнение продолжалось.
        """
        # Если было исключение, выводим информацию о нём (для отладки)
        if exc_type:
            print(exc_type)   # тип исключения, например, <class 'pymysql.err.IntegrityError'>
            print(exc_val)    # значение исключения, например, "Duplicate entry ..."

        # Проверяем, был ли создан курсор (а значит, и соединение)
        if self.cursor:
            # Если было исключение, откатываем транзакцию — все изменения отменяются
            if exc_type:
                self.conn.rollback()
            else:
                # Если исключений не было, фиксируем транзакцию — изменения становятся постоянными
                self.conn.commit()
            # Закрываем курсор и соединение, освобождая ресурсы
            self.cursor.close()
            self.conn.close()
        # Возвращаем True, чтобы подавить возможное исключение.
        # Это позволяет обработать ошибку в вызывающем коде (например, вернуть сообщение пользователю)
        # и предотвращает аварийное завершение программы.
        return True


